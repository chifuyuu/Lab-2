			<h2><p>PROBLEM:<br></h2>

First, if the building would be an INSTITUTIONAL BUILDING (e.g. Hospital), where in many people would always use the elevator daily. <br> Since there would be a lot of people using these elevators, it would be hard for them to go to a floor they wanted to go <br> to since it has a thousand floors and the elevator could also crash or won’t work because of too many people inside. <br>The space should be big enough wherein it could have a space for people to seat while waiting in the elevator.
<br>
Lastly, for the medical practitioners and workers in the Hospital. <br>Since, these people need to be prioritized for them to go to a specific part of the hospital.
<br>
<h2><br>
<br>SOLUTION:<br></h2>
<br>
<h3><br>Size:</h3>
<br>
Let’s say it’s a biggish elevator, can hold around 50 people, probably has a few seats for taking rest, and a TV screen (separate from the interface) to help with all the waiting.
<br>
<h3><br>Number of Elevators:</h3>
<br>
Since there would be many people going up and down in the building. It should have at least 50 elevators ready to accommodate the people who need using it.
<br>
<h3><br>Special Elevators:</h3>
<br>
There would be about 10 elevators which will be used for transferring patients and for the use of the medical practitioners. So they will able to go what floor they need to go in immediately.
<br>
<h3><br>Speed of the Elevator:</h3>
<br>
Let’s assume the speed to be around 22 mph (the Burj Khalifa’s top speed). This means it’d take around 8 and half minutes to get to the 1000th floor when travelling at top speed.
<br><br><br>
<h3>DESIGN:<br></h3>
<br>
This would how it looks like from the outside. Since we need at least 50 people inside the elevator it should be large enough where seats can also be place in the sides.
<br>
This would how it looks like inside the elevator itself. It should be comfortable enough so people won’t worry in using the elevators.
<br>
This image is from: https://www.pinterest.ph/pin/822399581929872631/
<br>
The interface would look like a keypad in a calculator with an LCD screen which you could just type in the floor number you want and there would also be an add and ‘x’ button. <br>After adding the floor, you would want to go you would likely see the floor of the building on the left LCD screen in light green.
<br><br><br>
<h2>Simple Summary:<br></h2>
<br>
1. 17 button numeric keypad with an LCD screen above it. Type in the floor number you want to go to, and then press add.
<br><br>
2. Flat TV screen for people to be entertained while waiting their destination. Since, it would take some time before they can go to what floor they want.
<br><br>
3. Air conditioner this would improve the over all comfort of the users inside the elevator. Since, it would be hot because there are 50 individuals maximum that will go inside the elevators.
<br><br>
4. An LCD screen to the right which shows a visual of the floors the elevator is going. Pressing “add” on the keypad adds your floor to the LCD screen, giving you feedback for your input. <br>Current floor is highlighted in a different colour (light green) and colour(orange) for floors added in the queue.
<br><br>
5. Open / close buttons for DOORS. The open button looks bigger intentionally. It’s one of the buttons I think a lot of people will need to look for immediately (for example, when someone yells “wait!!”).
<br><br>
6. Prv Fl button for private floors. (For administrators, workers and VIP’s in the building)
<br><br>
7. Card Reader After clicking the Prv Fl it then requires a card wherein the card will already have the floor the person is going to.
<br><br>
8. Alarm help buttons and a slot to insert the maintenance key<br><br>
